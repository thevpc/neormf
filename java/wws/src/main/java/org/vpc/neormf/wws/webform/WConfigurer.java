public final class WConfigurer {

    public static void setDateFormat(HttpSession session, SimpleDateFormat format){
        session.setAttribute("WConfigurer.DateFormat",format);
    }

    public static void setTimeFormat(HttpSession session, SimpleDateFormat format){
        session.setAttribute("WConfigurer.TimeFormat",format);
    }

    public static void setDateTimeFormat(HttpSession session, SimpleDateFormat format){
        session.setAttribute("WConfigurer.DateTimeFormat",format);
    }

    public static DateFormat getDateFormat(HttpSession session){
        SimpleDateFormat f=(SimpleDateFormat) session.getAttribute("WConfigurer.DateFormat");
        return f!=null ? f: DateFormat.getDateInstance();
    }

    public static DateFormat getTimeFormat(HttpSession session){
        SimpleDateFormat f=(SimpleDateFormat) session.getAttribute("WConfigurer.TimeFormat");
        return f!=null ? f: DateFormat.getTimeInstance();
    }

    public static DateFormat getDateTimeFormat(HttpSession session){
        SimpleDateFormat f=(SimpleDateFormat) session.getAttribute("WConfigurer.DateTimeFormat");
        return f!=null ? f: DateFormat.getDateTimeInstance();
    }
}
